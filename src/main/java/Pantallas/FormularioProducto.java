    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
                                     
    try {
        String nombre = txtNombre.getText().trim();
        double precio = Double.parseDouble(txtPrecio.getText().trim());
        int cantidad = Integer.parseInt(txtCantidad.getText().trim());

        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre del producto no puede estar vacío.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        cancelado = false;

        if (esEdicion) {
           
            if (elProducto == null) {
                JOptionPane.showMessageDialog(this, "Error: producto no cargado.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            elProducto.setNombreProducto(nombre);
            elProducto.setPrecioProducto(precio);
            elProducto.setCantidadProducto(cantidad);

            int registrosAfectados = inventarioDAO.actualizarProducto(elProducto);

            if (registrosAfectados > 0) {
                JOptionPane.showMessageDialog(this, "Producto actualizado correctamente.");

               
                if (ventanaPrincipal != null) {
                    ventanaPrincipal.actualizarListadoProductos();
                }

                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Error al actualizar el producto", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } else {
           
            Producto nuevoProducto = new Producto();
            nuevoProducto.setNombreProducto(nombre);
            nuevoProducto.setPrecioProducto(precio);
            nuevoProducto.setCantidadProducto(cantidad);

            int resultado = inventarioDAO.insertarProducto(nuevoProducto);

            if (resultado > 0) {
                JOptionPane.showMessageDialog(this, "Producto guardado correctamente.");

                if (ventanaPrincipal != null) {
                    ventanaPrincipal.actualizarListadoProductos(); 
                }

                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo guardar el producto.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Verifica que el precio y la cantidad sean válidos.", "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnGuardarActionPerformed

