    public void llenarDatosProducto() {
    if (elProducto == null) {
        System.out.println("ERROR: elProducto es null");
        return;
    }

    txtNombre.setText(elProducto.getNombreProducto());
    txtPrecio.setText(String.valueOf(elProducto.getPrecioProducto()));
    txtCantidad.setText(String.valueOf(elProducto.getCantidadProducto()));
}
    
